#!/usr/bin/env python3
"""
Step 01: Extract and prepare cognitive test data for RQ 7.3.3.

This step extracts cognitive test scores from dfnonvr.csv and calculates
totals for RAVLT, BVMT, and RPM tests. The data is prepared for subsequent
analysis of HCE predictors.

Generated by g_code for RQ 7.3.3 - Cognitive Predictors of HCE
"""

import sys
from pathlib import Path
import pandas as pd
import numpy as np

# ============================================================================
# RAVLT CEILING FIX AND PERCENT RETENTION FUNCTIONS
# ============================================================================

def fix_ravlt_ceiling(df, log_fn):
    """Fix RAVLT ceiling effects: substitute 15 for unadministered trials (stored as 0)."""
    trial_cols = [f'ravlt-trial-{i}-score' for i in range(1, 6)]
    fixes_applied = 0
    for idx in df.index:
        for i in range(1, 5):
            current_col = trial_cols[i]
            prev_col = trial_cols[i - 1]
            if df.at[idx, current_col] == 0 and df.at[idx, prev_col] >= 14:
                uid = df.at[idx, 'UID']
                df.at[idx, current_col] = 15
                fixes_applied += 1
                log_fn(f"[CEILING FIX] {uid}: {current_col} 0 -> 15 (prev trial = {df.at[idx, prev_col]})")
    log_fn(f"[CEILING FIX] Total fixes applied: {fixes_applied}")
    return df


def compute_ravlt_percent_retention(df, log_fn):
    """Compute RAVLT percent retention: delayed recall / best learning trial * 100."""
    trial_cols = [f'ravlt-trial-{i}-score' for i in range(1, 6)]
    dr_col = 'ravlt-delayed-recall-score'
    pct_ret = np.full(len(df), np.nan)
    for i, idx in enumerate(df.index):
        dr = df.at[idx, dr_col]
        denom = np.nan
        for trial_col in reversed(trial_cols):
            val = df.at[idx, trial_col]
            if val > 0:
                denom = val
                break
        if denom > 0:
            pct_ret[i] = (dr / denom) * 100
    n_valid = np.sum(~np.isnan(pct_ret))
    log_fn(f"[COMPUTED] RAVLT Percent Retention: {n_valid}/{len(df)} valid")
    return pct_ret

# Add project root to path for imports
PROJECT_ROOT = Path(__file__).resolve().parents[4]
sys.path.insert(0, str(PROJECT_ROOT))

# Import required tools
from tools.validation import validate_data_columns

# ============================================================================
# FILE PATHS
# ============================================================================
RQ_DIR = Path(__file__).resolve().parents[1]  # results/ch7/7.3.3
DATA_DIR = RQ_DIR / "data"
LOG_DIR = RQ_DIR / "logs"
OUTPUT_FILE = DATA_DIR / "step01_cognitive_tests.csv"
LOG_FILE = LOG_DIR / "step01_extract_cognitive_tests.log"
DATA_PATH = PROJECT_ROOT / "data" / "dfnonvr.csv"

# Create directories if they don't exist
DATA_DIR.mkdir(parents=True, exist_ok=True)
LOG_DIR.mkdir(parents=True, exist_ok=True)

# ============================================================================
# LOGGING
# ============================================================================
def log(msg):
    """Log to both file and console."""
    with open(LOG_FILE, 'a', encoding='utf-8') as f:
        f.write(f"{msg}\n")
        f.flush()
    print(msg, flush=True)

# Clear log file
with open(LOG_FILE, 'w', encoding='utf-8') as f:
    f.write("")

# ============================================================================
# MAIN PROCESSING
# ============================================================================
def main():
    """Main execution for Step 01."""
    log("[START] Step 01: extract_cognitive_tests")
    
    try:
        # Load raw data
        log("[LOAD] Loading dfnonvr.csv...")
        log(f"[LOAD] Data path: {DATA_PATH}")
        
        df_raw = pd.read_csv(DATA_PATH)
        log(f"[LOAD] Loaded {df_raw.shape[0]} participants, {df_raw.shape[1]} columns")
        
        # Initialize output dataframe
        output_df = pd.DataFrame()

        # Copy UID (keep original case for now, standardize later)
        output_df['uid'] = df_raw['UID'] if 'UID' in df_raw.columns else df_raw['uid']
        log("[COPY] UID column copied")

        # Apply RAVLT ceiling fix BEFORE computing totals
        ravlt_cols = ['ravlt-trial-1-score', 'ravlt-trial-2-score',
                      'ravlt-trial-3-score', 'ravlt-trial-4-score', 'ravlt-trial-5-score']
        ravlt_available = all(col in df_raw.columns for col in ravlt_cols)
        if ravlt_available:
            log("[CEILING FIX] Applying RAVLT ceiling fix...")
            df_raw = fix_ravlt_ceiling(df_raw, log)

        # Calculate RAVLT total (sum of 5 learning trials)
        if ravlt_available:
            output_df['ravlt_total'] = df_raw[ravlt_cols].sum(axis=1)
            log(f"[CALC] RAVLT total calculated from {len(ravlt_cols)} trials")
            log(f"[CALC] RAVLT total range: {output_df['ravlt_total'].min():.1f} to {output_df['ravlt_total'].max():.1f}")
        else:
            log("[WARNING] RAVLT columns not found - setting to NaN")
            output_df['ravlt_total'] = np.nan

        # Calculate BVMT total (3 learning trials ONLY - not delayed recall)
        bvmt_cols = ['bvmt-trial-1-score', 'bvmt-trial-2-score',
                     'bvmt-trial-3-score']

        # Check if BVMT columns exist
        bvmt_available = all(col in df_raw.columns for col in bvmt_cols)
        if bvmt_available:
            output_df['bvmt_total'] = df_raw[bvmt_cols].sum(axis=1)
            log(f"[CALC] BVMT total calculated from {len(bvmt_cols)} learning trials (delayed recall excluded)")
            log(f"[CALC] BVMT total range: {output_df['bvmt_total'].min():.1f} to {output_df['bvmt_total'].max():.1f}")
        else:
            log("[WARNING] BVMT columns not found - setting to NaN")
            output_df['bvmt_total'] = np.nan
        
        # Copy RPM score (already a total)
        if 'rpm-score' in df_raw.columns:
            output_df['rpm_score'] = df_raw['rpm-score']
            log("[COPY] RPM score copied from rpm-score column")
            log(f"[COPY] RPM score range: {output_df['rpm_score'].min():.1f} to {output_df['rpm_score'].max():.1f}")
        else:
            log("[WARNING] RPM score not found - setting to NaN")
            output_df['rpm_score'] = np.nan
        
        # Compute RAVLT percent retention
        if ravlt_available and 'ravlt-delayed-recall-score' in df_raw.columns:
            output_df['ravlt_pct_ret'] = compute_ravlt_percent_retention(df_raw, log)
            log(f"[CALC] RAVLT percent retention range: {output_df['ravlt_pct_ret'].min():.1f} to {output_df['ravlt_pct_ret'].max():.1f}")
        else:
            log("[WARNING] RAVLT delayed recall not found - setting ravlt_pct_ret to NaN")
            output_df['ravlt_pct_ret'] = np.nan

        # Extract BVMT percent retention from pre-computed column
        if 'bvmt-percent-retained' in df_raw.columns:
            output_df['bvmt_pct_ret'] = df_raw['bvmt-percent-retained']
            log(f"[COPY] BVMT percent retention copied from bvmt-percent-retained")
            log(f"[COPY] BVMT percent retention range: {output_df['bvmt_pct_ret'].min():.1f} to {output_df['bvmt_pct_ret'].max():.1f}")
        else:
            log("[WARNING] BVMT percent retention not found - setting to NaN")
            output_df['bvmt_pct_ret'] = np.nan

        # Copy demographics
        if 'age' in df_raw.columns:
            output_df['age'] = df_raw['age']
            log(f"[COPY] Age copied (range: {output_df['age'].min():.0f} to {output_df['age'].max():.0f})")
        else:
            log("[WARNING] Age not found - setting to NaN")
            output_df['age'] = np.nan
            
        if 'sex' in df_raw.columns:
            output_df['sex'] = df_raw['sex']
            log(f"[COPY] Sex copied (values: {output_df['sex'].unique()})")
        else:
            log("[WARNING] Sex not found - setting to NaN")
            output_df['sex'] = np.nan
            
        if 'education' in df_raw.columns:
            output_df['education'] = df_raw['education']
            log(f"[COPY] Education copied (range: {output_df['education'].min():.0f} to {output_df['education'].max():.0f})")
        else:
            log("[WARNING] Education not found - setting to NaN")
            output_df['education'] = np.nan
        
        # Handle NART (may not be available in preprocessed data)
        if 'nart-score' in df_raw.columns:
            output_df['nart_iq'] = df_raw['nart-score']
            log(f"[COPY] NART IQ copied from nart-score column")
            log(f"[COPY] NART IQ range: {output_df['nart_iq'].min():.0f} to {output_df['nart_iq'].max():.0f}")
        else:
            log("[INFO] NART data not available in dfnonvr.csv - adding null column as expected")
            output_df['nart_iq'] = np.nan
        
        # Data quality checks
        log("[VALIDATE] Running data quality checks...")
        
        # Check for missing values
        missing_summary = output_df.isnull().sum()
        log("[MISSING] Missing value summary:")
        for col in output_df.columns:
            n_missing = missing_summary[col]
            pct_missing = (n_missing / len(output_df)) * 100
            if n_missing > 0:
                log(f"  {col}: {n_missing} missing ({pct_missing:.1f}%)")
        
        # Validate using tools.validation
        log("[VALIDATE] Running validate_data_columns...")
        required_cols = ['uid', 'ravlt_total', 'bvmt_total', 'rpm_score', 'age', 'sex']
        validation_result = validate_data_columns(output_df, required_cols)
        
        if validation_result['valid']:
            log("[PASS] Data validation successful")
        else:
            log(f"[WARNING] Data validation issues: {validation_result.get('errors', [])}")
        
        # Save output
        log(f"[SAVE] Saving extracted cognitive test data to {OUTPUT_FILE}")
        output_df.to_csv(OUTPUT_FILE, index=False)
        log(f"[SAVED] Output shape: {output_df.shape}")
        log(f"[SAVED] Output columns: {list(output_df.columns)}")
        
        # Summary statistics
        log("[SUMMARY] Cognitive test statistics:")
        for col in ['ravlt_total', 'bvmt_total', 'rpm_score', 'ravlt_pct_ret', 'bvmt_pct_ret', 'age']:
            if col in output_df.columns and not output_df[col].isna().all():
                mean_val = output_df[col].mean()
                std_val = output_df[col].std()
                log(f"  {col}: Mean = {mean_val:.2f}, SD = {std_val:.2f}")
        
        log("[SUCCESS] Step 01 complete - cognitive tests extracted")
        
    except Exception as e:
        log(f"[ERROR] {str(e)}")
        log("[TRACEBACK] Full error details:")
        import traceback
        log(traceback.format_exc())
        sys.exit(1)

if __name__ == "__main__":
    main()